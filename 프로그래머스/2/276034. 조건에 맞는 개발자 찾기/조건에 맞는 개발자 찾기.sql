SELECT ID, EMAIL, FIRST_NAME, LAST_NAME
FROM DEVELOPERS AS D
WHERE D.ID IN (
    SELECT ID
    FROM SKILLCODES S
    WHERE S.NAME IN ('Python', 'C#') AND S.CODE & D.SKILL_CODE
)
ORDER BY ID

















